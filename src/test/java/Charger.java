public class Charger {
   private String brand;
   private String color;
   private double power;
   private double wireLength;
   private String forModels;


   public String getBrand() {
      return brand;
   }

   public void setBrand(String brand) {
      this.brand = brand;
   }

   public String getColor() {
      return color;
   }

   public void setColor(String color) {
      this.color = color;
   }

   public double getPower() {
      return power;
   }

   public void setPower(double power) {
      this.power = power;
   }

   public double getWireLength() {
      return wireLength;
   }

   public void setWireLength(double wireLength) {
      this.wireLength = wireLength;
   }

   public String getForModels() {
      return forModels;
   }

   public void setForModels(String forModels) {
      this.forModels = forModels;
   }
}
